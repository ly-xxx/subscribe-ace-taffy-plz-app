import { mkdir, readFile, writeFile, copyFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, '..');

const sourceScript = path.join(
  repoRoot,
  'node_modules',
  '@google',
  'model-viewer',
  'dist',
  'model-viewer-umd.min.js'
);
const sourceLicense = path.join(
  repoRoot,
  'node_modules',
  '@google',
  'model-viewer',
  'LICENSE'
);
const outputDir = path.join(repoRoot, 'src', 'vendor');
const outputModule = path.join(outputDir, 'modelViewerScript.ts');
const outputLicense = path.join(outputDir, 'model-viewer.LICENSE');

await mkdir(outputDir, { recursive: true });

const source = await readFile(sourceScript, 'utf8');
const safeSource = source.replace(/<\/script/gi, '<\\/script');

const moduleContents = `// Generated by \`npm run vendor:model-viewer\`.
// Source: @google/model-viewer/dist/model-viewer-umd.min.js
// License: Apache-2.0

export const modelViewerScript = ${JSON.stringify(safeSource)};
`;

await writeFile(outputModule, moduleContents, 'utf8');
await copyFile(sourceLicense, outputLicense);

console.log(`Wrote ${path.relative(repoRoot, outputModule)}`);
